alien_color = "Red"

# writing a if-else chain for different scenarios

if (alien_color.lower() == "green"):
    print("Green alien killed, Gained 5 points.")

elif (alien_color.lower() == "yellow"):
    print("Yellow alien killed, Gained 10 points.")

elif (alien_color.lower() == "red"):
    print("Red alien killed, Gained 15 points.")

else:
    print("No alien killed, Gained 15 points.s")